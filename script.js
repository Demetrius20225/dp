function navigate(page, addToHistory = true) {
    console.log("Navigating to:", page);
    if (addToHistory) {
        window.history.pushState({ page }, "", `#${page}`);
    }

    const content = document.getElementById("content");
    const filter = document.getElementById("filter");

    content.classList.add('hidden');

    setTimeout(() => {
        if (page === "home") {
            breadcrumbs = [{ name: "Головна", page: "home" }];
            content.innerHTML = `
                <h2>Вітаємо у нашому магазині!</h2>
                <p>Оберіть категорію або товар для перегляду.</p>
                <div class="button-group">
                    <button class="button" onclick="navigate('catalog')">Перейти до каталогу</button>
                    <button class="button" onclick="navigate('about')">Про нас</button>
                    <button class="button" onclick="navigate('contacts')">Контакти</button>
                </div>
                <div class="recommendations-container">
                    <h2>Рекомендації для вас</h2>
                    <div class="recommendations" id="home-recommendations"></div>
                </div>
            `;
            filter.style.display = "none";
            renderHomeRecommendations(); // Відображаємо рекомендації
        } else if (page === "about") {
            breadcrumbs = [{ name: "Головна", page: "home" }, { name: "Про нас", page: "about" }];
            content.innerHTML = `<h2>Про нас</h2><p>Наш магазин продає найкращу комп'ютерну техніку.</p>`;
            filter.style.display = "none";
        } else if (page === "contacts") {
            breadcrumbs = [{ name: "Головна", page: "home" }, { name: "Контакти", page: "contacts" }];
            content.innerHTML = `<h2>Контакти</h2><p>Email: shop@example.com<br>Телефон: +380123456789</p>`;
            filter.style.display = "none";
        } else if (page === "catalog") {
            breadcrumbs = [{ name: "Головна", page: "home" }, { name: "Каталог", page: "catalog" }];
            renderCatalog();
        } else if (page === "favorites") {
            breadcrumbs = [{ name: "Головна", page: "home" }, { name: "Вподобані", page: "favorites" }];
            toggleFavorites();
        } else if (page === "full-cart") {
            breadcrumbs = [{ name: "Головна", page: "home" }, { name: "Кошик", page: "full-cart" }];
            content.innerHTML = `
                <h2>Кошик</h2>
                <div id="full-cart-items"></div>
                <p>Загальна сума: <span id="full-cart-total">0</span> грн</p>
                <button class="button" onclick="toggleCheckout()">Оформити замовлення</button>
            `;
            renderFullCart(); // Відображаємо повний кошик
            filter.style.display = "none";
        } else if (products["Каталог"][page]) {
            breadcrumbs = [
                { name: "Головна", page: "home" },
                { name: "Каталог", page: "catalog" },
                { name: page, page: page }
            ];
            renderCategory(page);
        } else {
            // Перевірка на підкатегорії
            for (let category in products["Каталог"]) {
                if (products["Каталог"][category].товари && products["Каталог"][category].товари[page]) {
                    breadcrumbs = [
                        { name: "Головна", page: "home" },
                        { name: "Каталог", page: "catalog" },
                        { name: category, page: category },
                        { name: page, page: page }
                    ];
                    renderSubCategory(category, page);
                    return;
                }
            }
            navigate("home");
        }

        updateBreadcrumbs();

        setTimeout(() => {
            content.classList.remove('hidden');
        }, 300);
    }, 300);
}

function renderCatalog() {
    const content = document.getElementById("content");
    content.innerHTML = ""; // Очистити попередній контент

    let contentHTML = "<h2>Каталог</h2>";
    contentHTML += `<div class="catalog-grid">`;
    for (let category in products["Каталог"]) {
        const categoryData = products["Каталог"][category];
        contentHTML += `
            <div class="catalog-item">
                <img src="${categoryData.зображення}" alt="${category}">
                <h3>${category}</h3>
                <button onclick="renderCategory('${category}')">Переглянути</button>
            </div>
        `;
    }
    contentHTML += `</div>`;
    content.innerHTML = contentHTML;
    document.getElementById("filter").style.display = "none";

    // Додати анімацію
    content.classList.add('catalog-animation');
}

function renderCategory(category) {
    const content = document.getElementById("content");
    const filter = document.getElementById("filter");
    content.classList.add('hidden');

    setTimeout(() => {
        breadcrumbs = breadcrumbs.slice(0, 2);
        breadcrumbs.push({ name: category, page: category });

        let contentHTML = `<h2>${category}</h2>`;
        let items = products["Каталог"][category].товари;

        if (Array.isArray(items)) {
            contentHTML += `<div class="product-grid">`;
            contentHTML += items.map((product, index) => `
                <div class="product-item">
                    <img src="${product.зображення}" alt="${product.назва}" style="width: 100px; height: 100px;">
                    <h3>${product.назва}</h3>
                    <p>${product.ціна} грн</p>
                    <div class="product-actions">
                        <button onclick="openProduct('${category}', '', ${index})">Детальніше</button>
                        <button class="favorite-button" onclick="toggleFavorite('${category}', '', ${index})">
                            ${favorites.some(fav => fav.назва === product.назва) ? '❤️' : '🤍'}
                        </button>
                    </div>
                </div>
            `).join('');
            contentHTML += `</div>`;
            filter.style.display = "block";
        } else if (typeof items === "object") {
            contentHTML += `<div class="subcategory-grid">`;
            contentHTML += Object.keys(items).map(subCategory => `
                <div class="subcategory-item">
                    <img src="${items[subCategory].зображення}" alt="${subCategory}" style="width: 100px; height: 100px;">
                    <h3>${subCategory}</h3>
                    <button onclick="renderSubCategory('${category}', '${subCategory.replace(/'/g, "\\'")}')">Переглянути</button>
                </div>
            `).join('');
            contentHTML += `</div>`;
            filter.style.display = "none";
        } else {
            contentHTML = `<p>Категорія не знайдена</p>`;
        }

        content.innerHTML = contentHTML;
        updateBreadcrumbs();
        updateFilters(category);

        setTimeout(() => {
            content.classList.remove('hidden');
        }, 300);
    }, 300);
}

function renderSubCategory(category, subCategory) {
    const content = document.getElementById("content");
    content.classList.add('hidden');

    setTimeout(() => {
        breadcrumbs = [
            { name: "Головна", page: "home" },
            { name: "Каталог", page: "catalog" },
            { name: category, page: category },
            { name: subCategory, page: subCategory }
        ];

        let contentHTML = `<h2>${subCategory}</h2>`;

        // Отримуємо дані для підкатегорії
        const subCategoryData = products["Каталог"][category].товари[subCategory];

        if (subCategoryData && Array.isArray(subCategoryData.товари)) {
            const items = subCategoryData.товари;

            contentHTML += `<div class="product-grid">`;
            contentHTML += items.map((product, index) => `
                <div class="product-item">
                    <img src="${product.зображення}" alt="${product.назва}" style="width: 100px; height: 100px;">
                    <h3>${product.назва}</h3>
                    <p>${product.ціна} грн</p>
                    <div class="product-actions">
                        <button onclick="openProduct('${category}', '${subCategory.replace(/'/g, "\\'")}', ${index})">Детальніше</button>
                        <button class="favorite-button" onclick="toggleFavorite('${category}', '${subCategory.replace(/'/g, "\\'")}', ${index})">
                            ${favorites.some(fav => fav.назва === product.назва) ? '❤️' : '🤍'}
                        </button>
                    </div>
                </div>
            `).join('');
            contentHTML += `</div>`;
        } else {
            contentHTML = `<p>Товари не знайдено</p>`;
        }

        content.innerHTML = contentHTML;
        updateBreadcrumbs();
        updateFilters(category, subCategory);

        setTimeout(() => {
            content.classList.remove('hidden');
        }, 300);
    }, 300);
}
// Функція для додавання/видалення товару з вподобаних
