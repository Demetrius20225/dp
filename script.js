// Зміна кількості товару
function changeQuantity(index, amount) {
    cart[index].кількість += amount;
    if (cart[index].кількість <= 0) {
        cart.splice(index, 1);
    }
    updateCart(); // Оновлюємо кошик після зміни кількості
    renderFullCart(); // Оновлюємо повний кошик, якщо він відкритий
}
// Функція для видалення товару з кошика
function removeFromCart(index) {
    cart.splice(index, 1); // Видаляємо товар з кошика
    updateCart(); // Оновлюємо малий кошик
    renderFullCart(); // Оновлюємо великий кошик
}

// Функція для очищення кошика
function clearCart() {
    cart = []; // Очищаємо кошик
    updateCart(); // Оновлюємо малий кошик
    renderFullCart(); // Оновлюємо великий кошик
}
